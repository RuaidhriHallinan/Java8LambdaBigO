public class MathMaxLog {

    public static void main(String[] args)  {

        // get two double numbers
        double x = 60984.1;
        double y = -497.99;

        // get the natural logarithm for x
        System.out.println("Math.log(" + x + ")= " + Math.log(x));

        // get the natural logarithm for y
        System.out.println("Math.log(" + y + ")= " + Math.log(y));

        // get the greater of the two numbers
        System.out.println("Math.max(3, 27)= " +  Math.max(3, 27));

        // get the smaller of the two numbers
        System.out.println("Math.min(3, 27)= " +  Math.min(3, 27));

    }

}
